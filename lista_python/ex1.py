lista_vendas = [100, 80, 110, 800, 11]

print(lista_vendas[2])  # pegar um item da lista
print(lista_vendas[-1]) # pegar o último item da lista

# tamanho da lista
print(len(lista_vendas))

# somar todos os itens
total_vendas = sum(lista_vendas)
print(total_vendas)

# max, min, media
print(max(lista_vendas))
print(min(lista_vendas))
print(total_vendas / len(lista_vendas))

