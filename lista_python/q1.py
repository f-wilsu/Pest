vendas = [1500, 2000, 800, 3500, 1200]

total_vendas = sum(vendas)
qtde_dias = len(vendas)
media_vendas = total_vendas / qtde_dias

maior_venda = max(vendas)
menor_vendas = min(vendas)
print(f"Total: {total_vendas}")
print(f"Média: {media_vendas}")
print(f"Maior venda: {maior_venda}")
print(f"Menor venda: {menor_vendas}")
